package com.vti.entity;

public class Position {
	private int id;
	private String name;
	
	
	
	// set id
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
	
	
	// set Name
	public void setName(String name) {
		this.name = name;
	}
	
	// get name
	public String getName() {
		return name;
	}
}
