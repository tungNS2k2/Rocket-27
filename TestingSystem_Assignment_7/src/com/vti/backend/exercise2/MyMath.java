package com.vti.backend.exercise2;

public class MyMath {
	public static final Double PI = 3.14;
	
	public static double sum(int d) {
		
		return  d + PI;
	}
	
	public static int min(int a, int b) {
		if (a <= b) 
			return a;
		
		else 
			return b;
		
		
	}
	public static int sum(int a, int b) {
			return a + b;
	}
}
